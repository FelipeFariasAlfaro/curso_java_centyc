package clase5;

public class UsoDePrimitivos {

    public static void main (String[] arg)
    {
        System.out.println("=============================================");

        char caracter = 'M';
        System.out.println("Estoy declarando un char con valor: "+caracter);

        short numeroBreve = 32767;
        System.out.println("Estoy declarando un short con valor: "+numeroBreve);

        double grados;
        grados = 32.6;
        System.out.println("Estoy declarando un double con valor: "+grados);

        boolean estoyEnUnCursoDePython = false;
        System.out.println("¿Estoy en un curso de python? "+estoyEnUnCursoDePython);

        boolean estoyEnUnCursoDeJava = true;
        System.out.println("¿Estoy en un curso de Java? "+estoyEnUnCursoDeJava);

        int edad = 100;
        edad = 200;
        System.out.println("La edad es: "+edad);

    }

}
